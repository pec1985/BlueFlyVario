package com.bfv.view;

public class FieldUnits {
    public String name;
    public double multiplier;

    public FieldUnits(String name, double multiplier) {
        this.name = name;
        this.multiplier = multiplier;
    }

    public String getName() {
        return name;
    }

    public double getMultiplier() {
        return multiplier;
    }
}
